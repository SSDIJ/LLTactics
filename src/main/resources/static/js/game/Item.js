class Item {

    constructor(armor, damage, description, id, imageUrl, name, price, unit, velocity, life) {

        this.armor = armor;
        this.damage = damage;
        this.description = description;
        this.id = id;
        this.imageUrl = imageUrl;
        this.name = name;
        this.price = price;
        this.unit = unit;
        this.velocity = velocity;
        this.life = life;
    }
    
}

export default Item;